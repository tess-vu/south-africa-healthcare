SELECT 'RECORD COUNTS BY COMPANY SOURCE' AS VALIDATION;
SELECT COMPANY, COUNT(*) AS RECORD_COUNT 
FROM MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED 
GROUP BY COMPANY 
ORDER BY RECORD_COUNT DESC;

SELECT 'RECORDS WITH PRACTICE_NUM VS NULL' AS VALIDATION;
SELECT 
    CASE WHEN PRACTICE_NUM IS NOT NULL THEN 'Has PRACTICE_NUM' ELSE 'NULL PRACTICE_NUM' END AS PRACTICE_NUM_STATUS,
    COUNT(*) AS RECORD_COUNT
FROM MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED
GROUP BY PRACTICE_NUM_STATUS;

SELECT 'TOTAL RECORD COUNT' AS VALIDATION;
SELECT COUNT(*) AS TOTAL_RECORDS 
FROM MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED;

SELECT 'SAMPLE RECORDS' AS VALIDATION;
SELECT * 
FROM MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED 
LIMIT 20;
