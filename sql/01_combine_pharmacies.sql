-- CONSERVATIVE DEDUPLICATION USING PRACTICE_NUM
-- Deduplicates records WITH PRACTICE_NUM based on PRACTICE_NUM + COMPANY combination.
-- Keeps ALL records with NULL PRACTICE_NUM for spatial deduplication post-geocoding.
-- When duplicates exist, keeps record with longest ADDRESS as most complete for geocoding.
CREATE OR REPLACE TABLE MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED AS
SELECT 
    ROW_NUMBER() OVER (ORDER BY PROVINCE, PRACTICE_NAME, COMPANY) AS PHARMACY_ID,
    PROVINCE,
    LOCAL_MUNICIPALITY,
    DISTRICT_MUNICIPALITY,
    METROPOLITAN_MUNICIPALITY,
    CITY,
    PRACTICE_NAME,
    PRACTICE_NUM,
    PRACTICE_TYPE,
    ADDRESS,
    PHONE,
    FUNDING,
    COMPANY,
    COORDS
FROM (
    SELECT 
        *,
        CASE 
            WHEN PRACTICE_NUM IS NOT NULL THEN
                ROW_NUMBER() OVER (
                    PARTITION BY PRACTICE_NUM, COMPANY 
                    ORDER BY LENGTH(ADDRESS) DESC, PRACTICE_NAME
                )
            ELSE 1
        END AS row_num
    FROM MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED
)
WHERE row_num = 1;


-- EMPTY ROWS REMOVAL
-- Drops rows where both address and practice number are null.
DELETE FROM MUSA_DAIR_DB.INTERMEDIATE.PHARMACIES_COMBINED 
WHERE ADDRESS IS NULL 
AND PRACTICE_NUM IS NULL;


